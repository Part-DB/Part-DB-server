declare(strict_types=1);

namespace App\Tests\Services\Misc;

use App\Services\Misc\FAIconGenerator;
use App\Tests\Services\AmountFormatter;
use Symfony\Bundle\FrameworkBundle\Test\WebTestCase;

class FAIconGeneratorTest extends WebTestCase
{
    /**
     * @var FAIconGenerator
     */
    protected $service;

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub

        //Get an service instance.
        self::bootKernel();
        $this->service = self::getContainer()->get(FAIconGenerator::class);
    }

    public function fileExtensionDataProvider(): array
    {
        return [
            ['pdf', 'fa-file-pdf'],
            ['jpeg','fa-file-image'],
            ['txt', 'fa-file-lines'],
            ['doc', 'fa-file-word'],
            ['zip', 'fa-file-zipper'],
            ['png', 'fa-file-image'],
            ['jpg', 'fa-file-image'],
            ['gif', 'fa-file-image'],
            ['svg', 'fa-file-image'],
            ['xls', 'fa-file-excel'],
            ['xlsx', 'fa-file-excel'],
            ['ppt', 'fa-file-powerpoint'],
            ['pptx', 'fa-file-powerpoint'],
            ['docx', 'fa-file-word'],
            ['odt', 'fa-file-word'],
            ['ods', 'fa-file-excel'],
            ['odp', 'fa-file-powerpoint'],
            ['py', 'fa-file-code'],
            ['js', 'fa-file-code'],
            ['html', 'fa-file-code'],
            ['css', 'fa-file-code'],
            ['xml', 'fa-file-code'],
            ['json', 'fa-file-code'],
            ['yml', 'fa-file-code'],
            ['yaml', 'fa-file-code'],
            ['csv', 'fa-file-csv'],
            ['sql', 'fa-file-code'],
            ['sh', 'fa-file-code'],
            ['bat', 'fa-file-code'],
            ['exe', 'fa-file-code'],
            ['dll', 'fa-file-code'],
            ['lib', 'fa-file-code'],
            ['so', 'fa-file-code'],
            ['a', 'fa-file-code'],
            ['o', 'fa-file-code'],
            ['class', 'fa-file-code'],
            ['jar', 'fa-file-code'],
            ['rar', 'fa-file-zipper'],
            ['7z', 'fa-file-zipper'],
            ['tar', 'fa-file-zipper'],
            ['gz', 'fa-file-zipper'],
